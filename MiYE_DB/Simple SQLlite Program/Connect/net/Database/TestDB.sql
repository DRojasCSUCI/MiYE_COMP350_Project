CREATE TABLE USERS(USER_ID varchar(255), ADMIN_FLAG bool, F_NAME varchar(255), L_NAME varchar(255), GENDER varchar(255), DATE_START_OF_STAY varchar(255), DATE_END_OF_STAY varchar(255));
	
# ADMINS					
DROP TABLE IF EXISTS ADMINS;
CREATE TABLE ADMINS(USER_ID varchar(255), ADMIN_KEY varchar(255));

# SERVICES
DROP TABLE IF EXISTS SERVICES;
CREATE TABLE SERVICES(SERVICE_ID varchar(255), SERVICE_NAME varchar(255), SERVICE_DESC varchar(1020), PRICE_PER_MINUTE float, DURATION_OPTIONS varchar(255));

# USERS_ACTIVE_SERVICES
DROP TABLE IF EXISTS USERS_ACTIVE_SERVICES;
CREATE TABLE USERS_ACTIVE_SERVICES(USER_ID varchar(255), IN_SERVICE_FLAG bool, SERVICE_ID varchar(255), TIME_STARTED varchar(255), DURATION_PICKED int);

# USERS_SERVICES_HISTORY								
DROP TABLE IF EXISTS USERS_SERVICES_HISTORY;
CREATE TABLE USERS_SERVICES_HISTORY(USER_ID varchar(255), COMPLETED_FLAG bool, CANCELLED_FLAG bool, SERVICE_ID varchar(255), DATE_TIME varchar(255), DURATION_PICKED int, COST float);



